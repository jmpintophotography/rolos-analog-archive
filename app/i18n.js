const TRANSLATED_ATTRIBUTES = ["aria-label", "title", "placeholder"];
const SKIPPED_TAGS = new Set(["SCRIPT", "STYLE", "NOSCRIPT"]);

export function normalizeLanguage(value) {
  return String(value || "").toLowerCase().startsWith("en") ? "en" : "pt";
}

export function localeForLanguage(language) {
  return normalizeLanguage(language) === "en" ? "en-GB" : "pt-PT";
}

export function translatePhrase(value, language) {
  const target = normalizeLanguage(language);
  const source = String(value ?? "");
  if (!source.trim()) return source;
  const leading = source.match(/^\s*/)?.[0] || "";
  const trailing = source.match(/\s*$/)?.[0] || "";
  const core = source.slice(leading.length, source.length - trailing.length);
  const exact = target === "en" ? PT_TO_EN[core] : EN_TO_PT[core];
  if (exact) return `${leading}${exact}${trailing}`;

  for (const pattern of DYNAMIC_PATTERNS) {
    const match = core.match(target === "en" ? pattern.pt : pattern.en);
    if (match) return `${leading}${target === "en" ? pattern.toEn(match) : pattern.toPt(match)}${trailing}`;
  }

  const parts = core.split(/(\s+[·-]\s+)/);
  if (parts.length > 1) {
    let changed = false;
    const translatedParts = parts.map((part, index) => {
      if (index % 2) return part;
      const translated = translatePhrase(part, target);
      if (translated !== part) changed = true;
      return translated;
    });
    if (changed) return `${leading}${translatedParts.join("")}${trailing}`;
  }
  return source;
}

export function applyLanguage(root, language) {
  if (!root) return;
  const target = normalizeLanguage(language);
  const rootElement = root.nodeType === 1 ? root : root.documentElement;
  if (!rootElement) return;

  translateElement(rootElement, target);
  const walker = document.createTreeWalker(rootElement, NodeFilter.SHOW_ELEMENT | NodeFilter.SHOW_TEXT);
  let node = walker.nextNode();
  while (node) {
    if (node.nodeType === Node.TEXT_NODE) translateTextNode(node, target);
    else translateElement(node, target);
    node = walker.nextNode();
  }
}

export function startLanguageObserver(root, getLanguage) {
  if (!root || typeof MutationObserver === "undefined") return () => {};
  const observer = new MutationObserver((mutations) => {
    const language = normalizeLanguage(getLanguage?.());
    for (const mutation of mutations) {
      if (mutation.type === "characterData") {
        translateTextNode(mutation.target, language);
        continue;
      }
      mutation.addedNodes.forEach((node) => {
        if (node.nodeType === Node.TEXT_NODE) translateTextNode(node, language);
        else if (node.nodeType === Node.ELEMENT_NODE) applyLanguage(node, language);
      });
    }
  });
  observer.observe(root, {
    subtree: true,
    childList: true,
    characterData: true,
  });
  return () => observer.disconnect();
}

function translateTextNode(node, language) {
  const parent = node.parentElement;
  if (!parent || SKIPPED_TAGS.has(parent.tagName) || parent.closest("[data-i18n-skip]")) return;
  const translated = translatePhrase(node.nodeValue, language);
  if (translated !== node.nodeValue) node.nodeValue = translated;
}

function translateElement(element, language) {
  if (!element?.getAttribute || SKIPPED_TAGS.has(element.tagName) || element.closest("[data-i18n-skip]")) return;
  for (const attribute of TRANSLATED_ATTRIBUTES) {
    if (!element.hasAttribute(attribute)) continue;
    const value = element.getAttribute(attribute);
    const translated = translatePhrase(value, language);
    if (translated !== value) element.setAttribute(attribute, translated);
  }
}
